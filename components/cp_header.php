            <h1 class="font-weight-strong my-5">The Theremin Blog</h1>
            <article class="text-justify">
            <p>This blog intends to collect curiosities, diagrams, improvements, and everything related with this mystical 
                instrument, Therelabs is a project that mixes electronics with music. It was in 1994 that the odyssey of 
                building instruments and sound synthesis devices began. The projects covered in my electronics laboratory 
                were entirely in sound area, with many projects coming from magazines, from university colleagues and 
                mainly from internet research through the web. These include the projection and construction of Class A 
                amplifiers, class AB, PushPull, Bridge, speaker boxes and crossovers mainly in audio power. The most 
                fascinating part was in the design of analog devices and modules that altered the sound such as: 
                distortion, echo chambers, tremolos, rotary phase, parametric equalizers.</p>

            <p>It was at the heart of a rock band - Huckleberry Finn that the desire to play something exclusive, mythical,
                 and strange was born, it was then that I found the theremin. 

            <br>In the first instance I was looking for references from bands recordings that used this strange and 
            little-known instrument, I found the first samples from Beach Boys (good vibrations) it was strange, 
            I couldn't define the type of sound.

            <br>But it was in 1998 in a concert in England by Portishead that all the passion jumped out of my eyes when I
             saw live an instrument that was played in the air only by approaching the hands to its two antennas! my god I
              shivered, I had never seen such a thing, I remember that in the week after that concert I started my odyssey.</p>

            <h3 class="font-weight-light my-4">The decision.</h3>
            <p>My first experimental theremin was made from an integrated PLL phase-locked loop and a logical port on a
                 breadboard, this was my first contact with a theremin, it was digital and produced a hellish sound that
                  looked like a chainsaw. However, i took the challenge to try one of the circuits that I had downloaded: 
                  the "tube theremin".  At that time my knowledge of valves technologies was almost inexistent and i had 
                  to acquire all the required information to start this project.</p>
 
            <h3 class="font-weight-light my-4">Tube-Type Theremin construction Project.</h3>
            <p>When RCA introduced their version of Lev Termer's invention, they said it was as easy to play as humming. 
                While the sound that can be produced by the Theremin may some times sound like singing or like a violin, 
                the fact that you don't touch it to play it, removes the tactile feedback normally found when playing a 
                musical instrument.The musician stands motionless except for the slight movement of the hands, closer 
                to the pitch antenna.  The notes glide up scale, that is, further away a lower tone is produced. 
                The closer the hand is to the volume antenna, the louder the sound. (This is the reverse of the original
                 instrument, but I find it more intuitive to play this way.) Of the many Theremins that I have built the
                  ones based on tubes sound the best and have the most sensitivity to the players' hand positions. The
                   design presented here is the culmination of many years of experimentation.</p>
